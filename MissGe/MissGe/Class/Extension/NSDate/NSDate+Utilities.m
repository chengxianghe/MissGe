- (NSString *)formattedTimeDescription {
    
    NSDate *date = [self dateAtStartOfDay]; //今天 0点时间
    
    NSInteger hour = [self hoursAfterDate:date];
    
    //    NSLog(@"hour:%ld, 今天0点:%@",hour, [NSDate changeDateToLocalDate:date]);
    
    NSDateFormatter *dateFormatter = nil;
    
    //hasAMPM==TURE为12小时制，否则为24小时制
    NSString *formatStringForHours = [NSDateFormatter dateFormatFromTemplate:@"j" options:0 locale:[NSLocale currentLocale]];
    NSRange containsA = [formatStringForHours rangeOfString:@"a"];
    BOOL hasAMPM = containsA.location != NSNotFound;
    if (!hasAMPM) { //24小时制
        if (hour <= 24 && hour >= 0) {
            dateFormatter = [NSDateFormatter dateFormatterWithFormat:@"HH:mm"];
        }else if (hour < 0 && hour >= -24) {
            dateFormatter = [NSDateFormatter dateFormatterWithFormat:@"昨天 HH:mm"];
        }else {
            dateFormatter = [NSDateFormatter dateFormatterWithFormat:@"yyyy-MM-dd"];
        }
    }else {
        if (hour >= 0 && hour < 1) { // 00:00 - 01:00
            dateFormatter = [NSDateFormatter dateFormatterWithFormat:@"零点 00:mm"];
        }else if (hour >= 1 && hour < 6) { // 01:00 - 06:00
            dateFormatter = [NSDateFormatter dateFormatterWithFormat:@"凌晨 hh:mm"];
        }else if (hour >= 6 && hour < 12 ) { // 06:00 - 12:00
            dateFormatter = [NSDateFormatter dateFormatterWithFormat:@"上午 hh:mm"];
        }else if (hour >= 12 && hour < 13 ) { // 12:00 - 13:00
            dateFormatter = [NSDateFormatter dateFormatterWithFormat:@"中午 hh:mm"];
        }else if (hour >= 13 && hour < 18) { // 13:00 - 18:00
            dateFormatter = [NSDateFormatter dateFormatterWithFormat:@"下午 hh:mm"];
        }else if (hour >= 18 && hour < 24) { // 18:00 - 24:00
            dateFormatter = [NSDateFormatter dateFormatterWithFormat:@"晚上 hh:mm"];
        }else if (hour < 0 && hour >= -24){ // 18:00 - 24:00
            dateFormatter = [NSDateFormatter dateFormatterWithFormat:@"昨天 HH:mm"];
        }else { // 18:00 - 24:00
            dateFormatter = [NSDateFormatter dateFormatterWithFormat:@"yyyy-MM-dd"];
        }
        
    }
    
    NSString *ret = [dateFormatter stringFromDate:self];
    return ret;
}

